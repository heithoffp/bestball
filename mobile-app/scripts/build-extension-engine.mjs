// build-extension-engine.mjs — bundles the pure JS draft engine into a single
// IIFE for JavaScriptCore inside the broadcast extension. Output is committed
// (targets/draft-broadcast/assets/engine.js) so EAS builds don't depend on a
// bundling step. Re-run after ANY change under src/draft/ or shared/utils/:
//   npm run build:engine
//
// It ALSO emits src/draft/generated/engineSource.js — the SAME IIFE text as an
// importable JS string plus ENGINE_VERSION/ENGINE_BUILD. That module rides the
// app's JS bundle so the app can hand the current engine to the broadcast
// extension through the App Group (ADR-023 hot-load), letting parser fixes
// reach the extension via a JS reload with no native rebuild.
import { build } from 'esbuild';
import { fileURLToPath } from 'node:url';
import { readFile, writeFile, mkdir } from 'node:fs/promises';
import path from 'node:path';

const root = path.dirname(path.dirname(fileURLToPath(import.meta.url)));
const enginePath = path.join(root, 'targets/draft-broadcast/assets/engine.js');
const generatedPath = path.join(root, 'src/draft/generated/engineSource.js');

await build({
  entryPoints: [path.join(root, 'src/draft/extensionEngine.entry.js')],
  outfile: enginePath,
  bundle: true,
  format: 'iife',
  platform: 'neutral',
  // JavaScriptCore on iOS 16+ handles modern syntax; es2020 keeps output lean
  // while avoiding anything exotic.
  target: ['es2020'],
  banner: {
    js: '// GENERATED by scripts/build-extension-engine.mjs — do not edit.\n'
      + '// Source: src/draft/extensionEngine.entry.js (+ pure engine modules).',
  },
  minify: false,
  legalComments: 'none',
  logLevel: 'info',
});

// Read the identity out of the entry so engineSource.js stays in lockstep with
// the IIFE without re-parsing the bundle.
const entrySrc = await readFile(
  path.join(root, 'src/draft/extensionEngine.entry.js'), 'utf8',
);
const versionMatch = entrySrc.match(/ENGINE_VERSION\s*=\s*'([^']+)'/);
const buildMatch = entrySrc.match(/ENGINE_BUILD\s*=\s*(\d+)/);
if (!versionMatch || !buildMatch) {
  throw new Error('Could not read ENGINE_VERSION / ENGINE_BUILD from extensionEngine.entry.js');
}
const engineIife = await readFile(enginePath, 'utf8');

const generated = '// GENERATED by scripts/build-extension-engine.mjs — do not edit.\n'
  + '// The extension engine (targets/draft-broadcast/assets/engine.js) as an\n'
  + '// importable string, so the app can hot-load it into the App Group (ADR-023).\n'
  + `export const ENGINE_VERSION = ${JSON.stringify(versionMatch[1])};\n`
  + `export const ENGINE_BUILD = ${Number(buildMatch[1])};\n`
  + `export const ENGINE_SOURCE = ${JSON.stringify(engineIife)};\n`;
await mkdir(path.dirname(generatedPath), { recursive: true });
await writeFile(generatedPath, generated);
console.log(`  wrote ${path.relative(root, generatedPath)} (build ${Number(buildMatch[1])}, ${engineIife.length} bytes)`);
